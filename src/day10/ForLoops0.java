

package day10;

public class ForLoops0 {

    public static void main(String[] args) {
        forLoopExample();

    }

    private static void forLoopExample() {
        //        i++ is the same thing as i = i + 1;
        for (int i = 1; i <= 31; i+= 5) {
            System.out.println(i);
        }

        System.out.println("==================================================================" +
                "");
        for (int i = 5; i <= 6; i++) {
            System.out.println(i);
        }







    }}






